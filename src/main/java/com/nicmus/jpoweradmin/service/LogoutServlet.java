package com.nicmus.jpoweradmin.service;

import java.io.IOException;

import javax.servlet.ServletException;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import javax.servlet.http.HttpSession;

@WebServlet(name="logout", urlPatterns="/logout")
public class LogoutServlet extends HttpServlet {
	private static final long serialVersionUID = 1739569149494856794L;

	/* (non-Javadoc)
	 * @see javax.servlet.http.HttpServlet#doGet(javax.servlet.http.HttpServletRequest, javax.servlet.http.HttpServletResponse)
	 */
	@Override
	protected void doGet(HttpServletRequest req, HttpServletResponse resp) throws ServletException, IOException {
		this.processRequest(req, resp);
	}


	/**
	 * 
	 * @param httpServletRequest
	 * @param httpServletResponse
	 * @throws IOException 
	 */
	private void processRequest(HttpServletRequest httpServletRequest, HttpServletResponse httpServletResponse) throws IOException{
		HttpSession session = httpServletRequest.getSession();
		session.invalidate();
		httpServletResponse.sendRedirect("members/zones.jsf");
	}
	

}
